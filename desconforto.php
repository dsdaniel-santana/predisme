    <h2>Desconforto psicológico ou estresse contínuo</h2>
    <p>Desconforto psicológico ou estresse contínuo é um estado emocional negativo que ocorre quando uma pessoa enfrenta demandas ou pressões constantes, que ultrapassam sua capacidade de lidar. Essas demandas podem ser de ordem física, emocional ou psicológica, como excesso de trabalho, problemas financeiros, conflitos pessoais, entre outros.
