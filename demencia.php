        <h3 class="centralizar">Doenças progressivas</h3>
        <p> <b class="destaque borda-arredondada">Demência</b>é um termo usado para descrever um grupo de doenças
            progressivas que afetam a função cerebral. É caracterizada pela perda gradual de habilidades cognitivas,
            incluindo memória, raciocínio, linguagem, percepção e habilidades motoras.</p>
        <p>A demência pode ser causada por várias doenças e condições, incluindo a doença de Alzheimer, doença de
            Parkinson, derrame cerebral, lesões cerebrais traumáticas, entre outras. Os sintomas da demência podem
            variar, mas geralmente incluem problemas de memória, desorientação no tempo e no espaço, dificuldade em
            realizar tarefas cotidianas, mudanças de humor e comportamento, perda de interesse em atividades que
            costumavam ser prazerosas, entre outros.</p>
        <p>Infelizmente, a demência é uma doença progressiva e incurável. No entanto, existem tratamentos e cuidados que
            podem ajudar a melhorar a qualidade de vida da pessoa com demência e retardar a progressão da doença. Isso
            pode incluir medicações, terapia ocupacional, fisioterapia, psicoterapia e suporte emocional para o paciente
            e seus familiares.</p>

