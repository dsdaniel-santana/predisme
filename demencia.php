        <h3 class="centralizar">Quais são os sinais da Doença de Alzheimer?</h3>


        <p><b>O paciente com Alzheimer pode apresentar:</b></p>
        <p>
            Perda de memória recente com repetição das mesmas perguntas ou dos mesmos assuntos. Esquecimento de eventos,
            de compromissos ou do lugar onde guardou seus pertences. Dificuldade para perceber uma situação de risco,
            para cuidar do próprio dinheiro e de seus bens pessoais, para tomar decisões e para planejar atividades mais
            complexas. Dificuldade para se orientar no tempo e no espaço. Incapacidade em reconhecer faces ou objetos
            comuns, podendo não conseguir reconhecer pessoas conhecidas. Dificuldade para manusear utensílios, para
            vestir-se, e em atividades que envolvam autocuidado. Dificuldade para encontrar e/ou compreender palavras,
            cometendo erros ao falar e ao escrever. Alterações no comportamento ou na personalidade: pode se tornar
            agitado, apático, desinteressado, isolado, desinibido, inadequado e até agressivo. Interpretações delirantes
            da realidade, sendo comuns quadros paranoicos ao achar que está sendo roubado, perseguido ou enganado por
            alguém. Esquecer o que aconteceu ou o que ficou combinado pode contribuir para esse quadro. Alucinações
            visuais (ver o que não existe) ou auditivas (ouvir vozes) podem ocorrer, sendo mais frequentes da metade
            para o final do dia. Alteração do apetite com tendência a comer exageradamente, ou, ao contrário, pode
            ocorrer diminuição da fome. Agitação noturna ou insônia com troca do dia pela noite.Os sintomas não são os
            mesmos para todos os pacientes com demência, mesmo quando a causa de demência é a mesma. Nem todos os
            sintomas aparecerão em todos os pacientes. Como uma doença de curso progressivo, o quadro clínico do
            paciente com demência sofre modificações. Com a evolução da doença, há o aparecimento de novos sintomas ou o
            agravamento dos sintomas existentes.</p>

        <p><b>Outras demências</b></p>


        <p>Além da Doença de Alzheimer, existem muitos outros tipos de demência.</p>

        <p><b>Demência vascular</b></p>

        <p>
            Pode ser considerada a segunda maior causa de demência. A demência vascular é causada por lesões cerebrais
            de origem vascular e as manifestações clínicas dependem da localização e do número de lesões cerebrais. As
            lesões vasculares cerebrais podem ocorrer como infartos silenciosos, que não resultam em ataque reconhecido
            clinicamente, e como acidentes vasculares encefálicos, conhecidos popularmente como “derrame”. Os fatores de
            risco são: hipertensão arterial, diabetes mellitus, hipercolesterolemia, doença cardiovascular, fibrilação
            atrial, tabagismo, trombose, abuso de álcool e fatores genéticos.</p>

        <p><b>Demência com corpos de Lewy</b></p>


        <p>
            Causada pela presença de alterações cerebrais chamadas de corpos de Lewy. Os critérios para o diagnóstico
            clínico da doença incluem demência com sintomas da Doença de Parkinson (rigidez da musculatura, movimentos
            mais lentos; tremores são mais raros); alucinações visuais (ver coisas que não existem, geralmente pessoas,
            animais, objetos e crianças) e oscilação dos sintomas ao longo do dia. Os sintomas parkinsonianos e
            alucinações ocorrem no estágio inicial, quando alterações importantes na memória podem não ocorrer. Outras
            características da doença incluem quedas repetidas, desmaios, delírios (acreditar em coisas que não
            existem), outras formas de alucinações (auditivas, ouvir coisas que não existem) e sensibilidade importante,
            com reações adversas intensas, ao uso de medicações para delírios e alucinações (antipsicóticos).</p>


        <p><b>Demência na Doença de Parkinson</b></p>

        <p>
            Cerca de 40% dos pacientes com Doença de Parkinson podem evoluir para quadros demenciais. Também é causada
            pela presença de corpos de Lewy, mas estão presentes em locais diferentes do cérebro. Para esse diagnóstico,
            é necessário que o quadro demencial ocorra após um ano do início do quadro da Doença de Parkinson. Se a
            demência ocorrer em menos de um ano após o início dos sinais de Parkinson, a hipótese clínica principal
            passa a ser de demência por corpos de Lewy. A atenção é uma das funções mais prejudicadas. A memória também
            pode estar afetada, mas em um grau menos intenso do que é observado na Doença de Alzheimer. Outra capacidade
            muito comprometida é a de planejar, organizar e regular um comportamento motor (função executiva). Também
            são comuns os quadros de depressão e de alucinações visuais (ver animais, pessoas).</p>


        <p><b>Demência frontotemporal</b></p>

        <p>Quadro caracterizado por deterioração na personalidade e na cognição. A alteração típica no exame de
            neuroimagem é a redução da região frontal e temporal do cérebro. As mudanças no comportamento são mais
            importantes que os problemas na memória e orientação. As mudanças podem incluir: desinibição, impulsividade,
            inquietude, perda do julgamento, oscilação emocional, apatia, desinteresse, perda de motivação, isolamento,
            sentimentalismo excessivo, hipocondria, comportamento exaltado, choro fácil, risos inadequados,
            irritabilidade, comentários sexuais inadequados, atos indecentes, comportamento muito inadequado como urinar
            em público, alterações importantes do hábito alimentar (por exemplo, preferência por doces), negligência da
            higiene pessoal. As alterações cognitivas são menos evidentes que na Doença de Alzheimer e ocorrem após dois
            anos, aproximadamente, do início das alterações de comportamento. Ansiedade e depressão são comuns. O
            paciente pode apresentar atos violentos, comportamentos ruins que não apresentava antes da doença.</p>



