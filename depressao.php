    Já do ponto de vista técnico, a depressao é um quadro psiquiátrico marcado pela tristeza, falta de vontade, desânimo, alterações do sono e apetite, lentificação dos movimentos e velocidade de raciocínio, a ponto de a pessoa ter ou perceber um declínio da inteligência. Sensação de futuro sombrio e ruim e a falta de esperança, também é um sintoma comum. Nesses casos, mesmo que a pessoa se esforce para melhorar, não consegue. Isso pode trazer diversas consequencias para a vida do paciente acometido, como dificuldade em permanecer no trabalho e se relacionar, por exemplo. 
