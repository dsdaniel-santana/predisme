    </p>
    <p>
        Fatores psicológicos: condições de saúde mental, como ansiedade, depressão, transtornos de personalidade e trauma, podem aumentar o risco de desenvolver dependência de álcool e outras drogas. O uso de substâncias pode ser uma forma de automedicação para aliviar os sintomas desses problemas.
    </p>
    <p>
        Fatores ambientais: influências sociais e culturais também podem afetar o risco de desenvolver dependência de álcool e outras drogas. Fatores como acesso fácil às substâncias, pressão dos pares, estresse no trabalho ou em casa, trauma e falta de apoio social podem aumentar o risco de dependência.
